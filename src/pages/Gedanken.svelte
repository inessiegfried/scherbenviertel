    <h2>Im Viereck</h2>
    <section>
        <p>
            „He, Petra, Lust auf ein Eis?“. Juri lachte verschmitzt zu ihr
            rüber. „Hab grad keine Zeit. Muss ins Training. Tschüüüüsss.“ Petra
            schwang sich auf ihr Fahrrad und radelte los. „Hübsch sieht sie
            aus“, dachte Juri, „viel zu schade für Schröder.“ Er stand etwas
            unschlüssig vor dem Schulhaus und blickte dem kleiner werdenden Rad
            sehnsüchtig hinterher. „Hej, Alter, lass die Flanken nicht hängen.
            Wir gehen was saufen.“ Vor ihm hatte sich Bingo aus der 4f
            aufgebaut, der heute seinen freien Nachmittag hatte,  und hauchte
            ihm seine Bierfahne ins Gesicht. Juri machte eine abwehrende
            Handbewegung. „Mensch, Bingo, ich hab keine Lust auf deine
            Hopfentröstung. Die Frau will einfach nicht, ich habe meine Angel
            ausgeworfen, doch sie beisst einfach nicht an.“ „Vielleicht liegts
            an der Angel.“ Bingo lachte frivol. Juri trat einen Schritt zurück.
            „Also, ich geh dann mal, muss meinen Deutschvortrag fertig
            vorbereiten. Ich kann die Huber morgen nicht enttäuschen.
            Schliesslich bin ich die Stütze ihres Unterrichts.“
        </p>
        <p>
            Als er nochmals zurückschaute, sah er gerade, wie Schröder und die
            Neue das Schulhaus verliessen. Die Martha war seine Chance. Sie
            hatte die letzten drei Jahre mit ihren Eltern in Spanien gelebt.
            Der Schröder stand auf die, konnte hier seinen Helfertrip so
            richtig durchziehen. Betonte immer, dass es ihm auch so ergangen
            sei, als er damals nach seinem Amerikaaufenthalt repetieren musste.
            Die Petra war viel zu selbständig und um einiges gescheiter als der
            Schröder. Doch bei der Martha, da war Sozialhelfer Schröder in
            seinem Element. Dumm war nur, dass die Petra enorm eifersüchtig war
            und die etwas angefaulte Liebe zwischen ihr und Schröder gerade
            dadurch einen neuen Frühling erlebte. 
        </p>
        <p>
            Die beiden holten zu ihm auf. „Das war wieder ein Nachmittag! Noch
            ein Jahr den Henz in Geschichte und ich schnappe über.“ Juri wusste
            nicht, ob er angesprochen war oder ob der Schröder seine
            historische Unpässlichkeit der Martha anvertraute. „Mensch, Juri,
            lauf doch nicht so schnell.“ Schröder hetzte ihm hinterher mit
            Martha im Schlepptau. „Könnte ich deine Geschichtsnotizen haben, um
            die Probe vorzubereiten? Ich werd aus dem Gelabber vom Henz einfach
            nicht schlau.“ „Türlich, mach dir morgen eine Kopie davon.“
            Schröder war ein As in Mathe, aber sonst war nicht viel mit ihm,
            aber Juri wollte es mit ihm auch nicht verderben. „Kann ich dann
            auch eine Kopie bekommen. Ich schnall diese ganze 48er-Revolution
            einfach nicht“, ertönte nun Marthas warme Stimme. „Joachim hilft
            mir, wo er kann. Aber in Geschichte, du weisst ja..“  Juri lächelte
            gnädig. „Kein Problem.“ „Oh, unser Bus kommt. Bis morgen.“ Und weg
            waren sie.  Sie nannte ihn ‚Joachim‘, ein gutes Zeichen. Dann waren
            sie sich also schon näher gekommen. Mit federndem Schritt machte
            sich Juri auf den Heimweg. 
        </p>
    </section>

    <h2>Präpositionen</h2>
    <section>
        <p>
            Will denn der nicht endlich aufhören zu schwatzen? Und ich bin so
            müde. War wohl doch zu viel Rotwein gestern, ich vertrage einfach
            nicht mehr  so viel wie früher. Ja, das Alter. Jetzt muss ich wohl
            reagieren. Doch dann meint er wieder, ich sei männerfeindlich. Wie
            auch immer: „Lukas, konzentrieren Sie sich jetzt auf Ihre Arbeit.“
            So, das hält wohl die nächsten zehn Minuten. Collin gähnt schon
            wieder, hatte wohl ein heftiges Wochenende. „Angela, nehmen Sie
            jetzt endlich das Grammatikbuch hervor und beginnen Sie Ihre
            Arbeit! Ja, die Seiten 57 bis 59.“ Meine Nägel sind schön geworden.
            Super, diese French Manicure! „Paula, wenn Sie eine Frage haben,
            kommen Sie nach vorne.“ Ich hätte etwas zum Arbeiten mitnehmen
            sollen. „Lukas, jetzt hören Sie auf, mit dem Handy zu spielen und
            vertiefen Sie sich in das Thema.“ Vielleicht könnte ich den
            Decklack noch eine Spur dunkler wählen. „Angela, die Seiten 57 bis
            59, legen Sie das Handy weg!“ Ich achte auf die Wahl der richtigen
            Präposition und auf den Kasus, den diese Präposition verlangt. Eine
            klare Aufgabenstellung, die Übungen sind machbar. „Nein, Mischa,
            Sie dürfen jetzt nicht auf die Toilette.“ Alles Ausweichmanöver,
            nur, um nicht arbeiten zu müssen. Ah, jetzt kommt Paula doch noch,
            um etwas zu fragen. Nein, sie wäscht sich nur die Hände und trinkt
            Wasser. Die sind ja dauernd am Verdursten, sagen an den Fläschchen
            wie Kleinkinder. Wenn es doch endlich klingeln würde! „Lukas, es
            reicht jetzt! Setzen Sie sich da nach vorne, nehmen Sie Ihr Buch
            und arbeiten Sie ENDLICH!“ Und für den Tai-Curry brauche ich diesen
            Basmati-Beutel-Reis, 250g Pouletbrust und chinesisches Gemüse.
            Kokosmich und Curry-Paste habe ich noch. Ah, jetzt hat es Collin
            doch noch gerafft, dass es etwas zu tun gibt. „Ja, Nora, Sie müssen
            den Satz mit der Präposition und dem Artikel im richtigen Kasus
            ergänzen.“ Ich kaufe im Globus noch diese roten Stäbchen, die
            würden gut aussehen zum schwarzen Geschirr.  Da hinten schwatzt es
            schon wieder. Dieses Zischen tut meinen Ohren weh. „Aaangelaaa!“
            Ich erkläre gerade dem Remo, wie er die Aufgaben lösen soll. Immer
            diese Ausreden. Dabei reden sie übers Wochenende, geben an mit
            ihren Saufgelagen. Diese Stille plötzlich, jetzt arbeiten sie doch
            noch konzentriert. Jetzt darf einfach niemand mehr etwas fragen,
            sonst geht das Geraune gleich wieder los.
        </p>
    </section>

    <h2>Wiederbeleben</h2>
    <section>
        <p>
            Das Zimmer war kahl. Ein Tisch, ein Stuhl, ein Schrank, ein Bett.
            Sonst nichts. Auf dem Rost des Gitterbettes lag eine Matratze in
            einem gestreiften Blau. Dahinter löste sich die Tapete von der
            Wand. Von der Decke hing ein Kabel mit einer Glühbirne, deren Licht
            die ganze Hässlichkeit des Zimmers preisgab. „Da sind wir“, sagte
            die Alte. Ihr zahnloser Mund lächelte mir blöde entgegen. „Gefällt
            es Ihnen?“ Ja, es war genau das, was ich gesucht hatte.
        </p>
        <p>
            Ich stellte meine Tasche aufs Bett und begann auszupacken. Viel
            hatte ich nicht dabei, etwas Wäsche zum Wechseln, einen warmen
            Pullover, die notwendigsten Toilettenartikel. Keine Ablenkung, das
            war das Wichtigste. 
        </p>
        <p>
            Nachdem ich meine wenigen Habseligkeiten in den Schrank geräumt
            hatte, legte ich mich auf die Matratze, die Schuhe behielt ich an.
            Ich starrte auf den Wasserflecken an der Decke, auf die Fliege, die
            die Lampe umkreiste, spürte das unbequeme Bett, setzte mich auf,
            legte mich wieder hin. Irgendwann schlief ich ein und schreckte
            dann jäh auf aus einem Traum, an den ich mich schon beim Aufwachen
            nicht mehr erinnern konnte. Jetzt war es stickig heiss, die Lampe
            brannte immer noch, die Fliege kreiste immer noch. Ich verliess das
            Zimmer, ging den dunklen Korridor entlang, fand die Tür zur
            Toilette, kehrte wieder zurück und schlief wieder ein. 
        </p>
        <p>
            Am Morgen wartete ich, bis die Alte mir das Frühstück gebracht
            hatte. Dann setzte ich mich an den Tisch, ass zwei Stück Brot,
            trank den scheusslichen Kaffee und legte mich wieder aufs Bett. Die
            Lampe brannte nicht mehr und die Fliege sass am Rand der
            Kaffeetasse und putzte sich ihre Flügel. Von draussen drang das
            Tageslicht durch die ungeputzten Scheiben. Viel gab es nicht her,
            das Zimmer lag hofseitig. Zwischendurch setzte ich mich auf, legte
            mich dann wieder hin, nickte ein, wachte auf, stand auf und machte
            mich auf den Weg zur Toilette.
        </p>
        <p>
            Irgendwann brachte mir die Alte das Mittagessen. Ich setzte mich an
            den Tisch, ass das Brot, schlürfte die Suppe und legte mich dann
            wieder aufs Bett. 
        </p>
        <p>
            Vielleicht  war es doch keine so gute Idee gewesen, hierher zu
            kommen. Natürlich hatte ich wissen wollen, wie sich mein Vater
            gefühlt hatte, hatte nachempfinden wollen, was er durchgemacht
            hatte. Jetzt, wo ich die Wahrheit über ihn kannte. 
        </p>
        <p>
            Im Zimmer war es wieder dunkel. Die Alte klopfte, zündete die Lampe
            an, stellte das Abendessen auf den Tisch und schlurfte wortlos
            wieder hinaus. Keine Gespräche, so hatten wir es vereinbart. Ich
            setzte mich an den Tisch, ass das Brot, liess den unappetitlichen
            Käse links liegen, schlürfte den Kaffee, an den ich mich fast nicht
            gewöhnen konnte und legte mich wieder aufs Bett. Die Fliege drehte
            ihre Runden. 
        </p>
        <p>
            Wie lange würde ich das aushalten? 12 Jahre wie er? Irgendwann
            schlief ich ein, wachte auf, ging auf die Toilette, legte mich
            wieder hin, schlief ein, wachte gegen Morgen auf und wartete auf
            mein Frühstück. Die Fliege surrte am Fenster. 
        </p>
        <p>
            Ein schöner Mann soll er gewesen sein, als sie sich kennen lernten,
            doch als er zurückkam, hatte sie ihn nicht mehr erkannt. Bald
            darauf war er gestorben und sie war schwanger zurückgeblieben, war
            dann zurückgekehrt in ihr Dorf und hatte die Schande eines
            unehelichen Kindes wortlos ertragen. Was hätte sie ihnen sagen
            sollen, was mir? Dass sie sich verliebt hatte in einen Kommunisten?
            Die waren nach dem Krieg noch verhasster geworden. Dass sie auf ihn
            gewartete hatte, zu ihm gehalten, geschwiegen hatte? 
        </p>
        <p>
            Was hätte sie mir sagen sollen? Alles, ja, wirklich alles, wie sie
            ihn kennen gelernt hatte, wie sich Grübchen bildeten, wenn er
            lächelte, wie seine Augen strahlten, wenn er sie ansah, wie sie
            sich geliebt hatten. Wie sie gehofft hatte, dass er sie nicht
            vergessen würde. Wie sie sich ängstigte Tag und Nacht. Wie sie auch
            in den Bombennächten mehr um sein als um ihr Leben bangte. Wie
            glücklich und unendlich traurig sie war, als er wiederkam. Wie er
            sich an sie geklammert hatte in der ersten Nacht, in der sie wieder
            beisammen lagen. Was er ihr erzählt hatte von der Gefangenschaft.
            Wie er immer schwächer geworden war. Wie sie an seinem Sterbebett
            gewacht hatte bis zum letzten Atemzug. 
        </p>
        <p>
            Ich stand auf, packte meine Sachen und ging ohne Frühstück. Ich
            hatte begriffen. Die Geschichte meiner Eltern war auch meine
            Geschichte. Sie strömte durch mein Blut, pulsierte in meinen Adern,
            alles Erzählen und Nachempfinden machte sie zur Fiktion. 
        </p>
    </section>

